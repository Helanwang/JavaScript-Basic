const person = {}

person.firstname = 'Helen'
person.lastname = 'Wang'
person.age = 50
person.eyecolor = 'blue'

console.log(person)
// example of creating an empty JAVA object

// You can access object properties in two ways:
console.log(person.age)
// output: 50
console.log(person['age'])
// output: 50

